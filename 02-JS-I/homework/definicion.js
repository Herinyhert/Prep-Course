//Strins:
//Es un conjunto de texto encerrado entre comillas simples ('')ó dobles (""),
//el mismo se refiere a un tipo de contenido de una variable.

//Funciones:
//Se refieren a soluciones de problemas o pedidos, su funcion es simplificar
//el proceso de escritura en nuestro archivo

//Argumento:
//Definimos como argumentos cuando queremos utilizar una variable dentro de una
//funcion, donde puede ser identificada antes o despues de la funcion.

//Return:
//Se utiliza return cuando solicitamos a la funcion que nos devuelva un valor,
//es importe tener en cuenta que una vez que le indicamos a la función return la misma
//se detiene y no continua la ejecución

